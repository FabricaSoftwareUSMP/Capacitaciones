package beans;

public class ProductoBean {
	
	int id;
	String nombre_cliente;
	int hambuerguesa_id;
	int papas_id;
	double precio;
	int crema_id;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNombre_cliente() {
		return nombre_cliente;
	}
	public void setNombre_cliente(String nombre_cliente) {
		this.nombre_cliente = nombre_cliente;
	}
	public int getHambuerguesa_id() {
		return hambuerguesa_id;
	}
	public void setHambuerguesa_id(int hambuerguesa_id) {
		this.hambuerguesa_id = hambuerguesa_id;
	}
	public int getPapas_id() {
		return papas_id;
	}
	public void setPapas_id(int papas_id) {
		this.papas_id = papas_id;
	}
	public double getPrecio() {
		return precio;
	}
	public void setPrecio(double precio) {
		this.precio = precio;
	}
	public int getCrema_id() {
		return crema_id;
	}
	public void setCrema_id(int crema_id) {
		this.crema_id = crema_id;
	}
	
	
	
	

}
